package com.advanceamerica.graphqlcustomer.model;

public class DmaTupleType {

    protected String dmaCode;

    protected String dmaDescription;

    /**
     * @return the dmaCode
     */
    public String getDmaCode() {
        return dmaCode;
    }

    /**
     * @param dmaCode the dmaCode to set
     */
    public void setDmaCode(String dmaCode) {
        this.dmaCode = dmaCode;
    }

    /**
     * @return the dmaDescription
     */
    public String getDmaDescription() {
        return dmaDescription;
    }

    /**
     * @param dmaDescription the dmaDescription to set
     */
    public void setDmaDescription(String dmaDescription) {
        this.dmaDescription = dmaDescription;
    }   

}